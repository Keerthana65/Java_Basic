package sorting;

/**
 *
 * @author Keerthana
 */
public class JavaSwap {
    public static void main(String[] args) {
        int a = 5, b = 3;
        
        System.out.println("Value of a before swap:"+a);
        System.out.println("Value of a before swap:"+b);
        
       int a1 = 5+b-a;
       int b1 = 3+a-b;
        
        System.out.println("Value of a after swap:"+a1);
        System.out.println("Value of a after swap:"+b1);
    }
}
